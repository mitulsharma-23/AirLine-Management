    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String FirstName = firstname.getText();
        String LastName = lastname.getText();
        String Passport = passport.getText();
        String Nic = nic.getText();
        String Address = address.getText();
        
        
        
        String Gender = "";
        if(male.isSelected()){
            Gender = "Male";
        }else{
            Gender = "Female";
        }
        
        String Contact = contact.getText();
        String CustomerId = CustomerID.getText();
        
        DateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
        String date = dt.format(dob.getDate());
        
        try{                                         
            // TODO add your handling code here:
            try{
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex){
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/airlinedatabase", "root", "mitulsharma");
            pre = con.prepareStatement("Insert into Customers(CustomerID, FirstName, LastName, Passport, NICID, Address, DOB, Contact, Gender)values(?, ?, ?, ?, ?, ?, ?, ?, ?)");
            
            pre.setString(1, CustomerId);
            pre.setString(2, FirstName);
            pre.setString(3, LastName);
            pre.setString(4, Passport);
            pre.setString(5, Nic);
            pre.setString(6, Address);
            pre.setString(7, date);
            pre.setString(8, Contact);
            pre.setString(9, Gender);
            
            pre.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Customer Added");
            
        } catch (SQLException ex){
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

